/**
 * Constructs a new instance of a FloodFilledImage with a image `png`.
 * 
 * @param png The starting image of a FloodFilledImage
 */
FloodFilledImage::FloodFilledImage(const PNG & png) {
  /** @todo [Part 2] */
  this->png=png;
}

/**
 * Adds a FloodFill operation to the FloodFillImage.  This function must store the operation,
 * which will be used by `animate`.
 * 
 * @param traversal ImageTraversal used for this FloodFill operation.
 * @param colorPicker ColorPicker used for this FloodFill operation.
 */
void FloodFilledImage::addFloodFill(ImageTraversal & traversal, ColorPicker & colorPicker) {
  /** @todo [Part 2] */
  this->traversal.push_back(&traversal);
  this->colorPicker.push_back(&colorPicker);
}

/**
 * Creates an Animation of frames from the FloodFill operations added to this object.
 * 
 * Each FloodFill operation added by `addFloodFill` is executed based on the order
 * the operation was added.  This is done by:
 * 1. Visiting pixels within the image based on the order provided by the ImageTraversal iterator and
 * 2. Updating each pixel to a new color based on the ColorPicker
 * 
 * While applying the FloodFill to the image, an Animation is created by saving the image
 * after every `frameInterval` pixels are filled.  To ensure a smooth Animation, the first
 * frame is always the starting image and the final frame is always the finished image.
 * 
 * (For example, if `frameInterval` is `4` the frames are:
 *   - The initial frame
 *   - Then after the 4th pixel has been filled
 *   - Then after the 8th pixel has been filled
 *   - ...
 *   - The final frame, after all pixels have been filed)
 */ 
Animation FloodFilledImage::animate(unsigned frameInterval) const {
  Animation animation;
  /** @todo [Part 2] */
  PNG temp=this->png;
  for(unsigned int i=0; i<traversal.size(); i++)
  {
    ImageTraversal* temp2=traversal[i];
    ImageTraversal::Iterator start=temp2->begin();
    ImageTraversal::Iterator end=temp2->end();
    unsigned int x=0;
    for(ImageTraversal::Iterator iter=start; iter!=end; ++iter)
    {
      if(x%frameInterval==0)
      {
        animation.addFrame(temp);
      }
      x=x+1;
      HSLAPixel &cur=temp.getPixel((*iter).x, (*iter).y);
      cur=colorPicker[i]->getColor((*iter).x, (*iter).y);
    }
  }
  animation.addFrame(temp);
  return animation;
}
